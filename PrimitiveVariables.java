public class PrimitiveVariables{



    public static void main(String...args){
        System.out.println("Primitive variable app has started! ");

        char singleCharacterVariable = 'A';
        System.out.println("char value = "+singleCharacterVariable);

        short shortVariable = 2000;
        System.out.println("short value = "+shortVariable);

        int integerVariable = 500;
        System.out.println("int value = "+integerVariable);




    
    }
}